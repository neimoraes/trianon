         </a>
         <div class="underline-tn">
         </div>
        <div class="collapse" id="collapseimunologia">
          Habitualmente, é recomendado um período de jejum para a coleta de sangue para exames laboratoriais. Após as refeições, a circulação de certos elementos provenientes da alimentação pode interferir em algumas metodologias laboratoriais e prejudicar a realização do exame. Alguns exames sofrem influência da dieta diária prévia à coleta, mesmo respeitando-se o período regulamentar de jejum, sendo um exemplo bem conhecido a dosagem de triglicérides que apresenta variações importantes conforme a característica da dieta prévia.
