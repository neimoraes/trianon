        <h3 class="h5 py-3 m-0">Alguns médicos dispensam jejum para alguns exames e pelas instruções de alguns laboratórios é necessário. Qual orientação seguir?</h3>
         </a>
         <div class="underline-tn">
         </div>
        <div class="collapse" id="collapsehormonios">
          <div class="whitespace-tn text-left"> A boa prática laboratorial recomenda que, para a maioria dos exames de sangue, a coleta seja realizada após um período mínimo de quatro horas de jejum, para o indivíduo adulto. Crianças e recém-nascidos devem ter este prazo reduzido ou até mesmo abolido, dependendo de cada situação clínica. Para cada exame, porém, pode haver necessidade de orientação específica, pois a concentração das substâncias absorvidas, como a glicose, por exemplo, varia de acordo com o tempo após a ingestão do alimento. Em contrapartida, um tempo de jejum muito prolongado também causa variações. Cada paciente, cada exame e cada situação devem ter suas particularidades analisadas de forma a se obter o maior grau de confiabilidade dos resultados. Em caso de dúvidas, consulte sempre o seu médico.
