        <a class="d-flex align-items-center" data-toggle="collapse" href="#collapseroxo" role="button" aria-expanded="false" aria-controls="collapseroxo">
        <h3 class="h5 py-3 m-0">Por que quando se tira sangue, às vezes o local fica roxo?</h3>
         </a>
         <div class="underline-tn">
         </div>
        <div class="collapse" id="collapseroxo">
          A formação de hematoma é uma situação comum após a punção de artérias e veias. O hematoma origina-se do extravasamento do sangue para o tecido sob a pele, durante ou após a punção. Caso a formação do hematoma seja identificada durante a punção deve-se fazer compressão local durante pelo menos dois minutos. Compressas frias podem ser utilizadas por 24 horas para auxiliar a reduzir a dor local e a progressão do hematoma. O procedimento de dobrar o braço após a retirada da agulha e/ou carregar objetos relativamente pesados logo após a coleta, contribuem sobremaneira para a formação do hematoma mesmo após uma coleta de sangue bem sucedida.
