  <h3 class="py-4">Remédio prescrito pode reprovar no exame toxicológico?</h3>

  <p class=" whitespace-tn">Nem todas as substâncias detectadas pelo exame toxicológico são ilícitas.

  Algumas substâncias podem ser prescritas por um médico para tratamentos específicos.

  Mas então, como deve proceder um motorista que faz uso de medicamento composto à base de substância detectável no toxicológico?

  Nesses casos, o motorista deverá apresentar a receita médica no momento da coleta.

  Além disso, a Lei 13.103 (Lei do Motorista), demanda que o condutor mostre a receita do medicamento no momento do exame de vista.

  Desta forma, ficará devidamente atestado que o consumo da substância é justificada pelo uso de medicamento para tratamento médico, a fim de impedir que o motorista reprove no exame toxicológico.
  </p>
  <h3 class="py-4">Álcool e cigarro são detectados pelo exame toxicológico?</h3>
  <p class=" whitespace-tn">Alcool no exame toxicológico

  A Lei 13.103 não exige a detecção do consumo de energéticos, antidepressivos, álcool, anabolizantes, calmantes e similares.

  Portanto, o exame não analisa o consumo destas substâncias.</p>
  <a class="" data-toggle="collapse" href="#collapsescabelo" role="button" aria-expanded="false" aria-controls="collapsescabelo">
    <h3 class="py-2 green-text-tn underline-tn">Exame toxicológico de cabelo ou de pelo. Qual é a diferença?</h3>
  </a>
  <div id="collapsescabelo" class="collapse pl-4 underline-tn">
  <p class=" whitespace-tn">O exame toxicológico é baseado em amostras de queratina de cabelos, pelos do corpo (pernas, braços e axilas).

  Portanto, é o único método de comprovação de consumo de droga, aceito pelo DENATRAN – Departamento Nacional de Trânsito, pois o toxicológico é a única forma de atestar consumo ou abstinência da substância psicoativa num período de 90 dias.

  Visto que o período mínimo para análise toxicológica é de 90 dias, o exame de larga janela torna-se o único a atender a Lei 13.0103, (Lei do Motorista).

  Por conseguir analisar o consumo de substâncias de um longo período é chamado de exame de larga janela de detecção.

  Entretanto, há uma diferença entre o exame realizado por meio da análise de cabelo e o exame realizado em amostras de pelo, que é justamente: a janela de detecção.

  Antes de mais nada, a diferença se dá pelo processo de crescimento de cabelos e pelos é diferente.

  Enquanto os cabelos crescem de forma contínua, os pelos do corpo crescem de forma cíclica, alternando constantemente ciclos de crescimento, pausa e queda.</p>
  <h5 class="py-4">Pelos</h5>

  <p class=" whitespace-tn">Um pelo corporal completa seu ciclo total, chegando à queda, em aproximadamente em 6 meses, ou 180 dias. Por esta razão, o exame toxicológico realizado por meio de amostra de pelo, não apontará análise com período inferior a 180 dias.
  </p>
  <h5 class="py-4">Cabelos</h5>

  <p class=" whitespace-tn">Já o cabelo, por ter um crescimento contínuo, tem a janela de detecção determinada pelo comprimento, a contar da raiz do cabelo. Só para ilustrar, cabelo cresce, em média, 1.3 cm por mês. Portanto, para conseguir analisar um período de 90 dias, é necessário que o cabelo tenha o comprimento mínimo de 3 cm.
  </p>
  <h5 class="py-4">Unhas</h5>

  <p class=" whitespace-tn">É importante destacar que os testes com amostras de unhas são realizados apenas quando o candidato apresenta condições clínicas que o impossibilitam dispor de outro tipo de amostra.

  Por exemplo, no caso de pessoas que sofrem de alopecia universal, ou alguma outra condição que impeça o crescimento de pelos ou cabelos. Posto que a alopecia universal é uma desordem autoimune, caracterizada pela perda total de cabelo no couro cabeludo e de pelos no corpo.

  Vale lembrar que testes toxicológicos feitos a partir de amostras de fluídos corporais, como sangue e urina, por exemplo, não são aceitos no processo de renovação da CNH. A janela de detecção de outros métodos de exame toxicológico (urina / sangue), são curtas e não atendem às exigências da Lei 13.103/2015, (Lei do Motorista).
  </p>
  </div>
  <h3 class="py-4">Ficar perto de quem consome droga afeta o resultado do exame?</h3>

  <p class=" whitespace-tn">O simples fato de permanecer próximo de alguém que esteja consumindo droga, não oferece risco de alteração no resultado do exame.

  Ainda assim, a Toxicologia Pardini toma todas as precauções quanto a descontaminação das amostras, para garantir a legitimidade do resultado. Logo que a amostra chega para análise, ela passa por um processo intensivo de repetidas lavagens.

  Portanto, nada que tenha sido depositado na superfície do cabelo ou pelo é considerado no momento da análise toxicológica.

  Desta forma, somente a parte interna do cabelo, onde se depositam os metabólitos, por meio da irrigação sanguínea, é analisada.

  Por estas medidas de segurança, o exame toxicológico da Toxicologia Pardini é o mais confiável.</p>
