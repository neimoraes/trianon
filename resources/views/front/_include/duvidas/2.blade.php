          <div class="whitespace-tn text-left">Claro. Alguns exames, aliás, são solicitados exatamente porque a pessoa está com febre. A intenção é verificar se alguma infecção é a responsável. Porém, em algumas circunstâncias, a doença responsável pela febre pode interferir nos exames destinados a avaliar aspectos metabólicos e imunológicos. Por cautela, conseqüentemente, consulte o seu médico ou o laboratório antes de fazer o exame. </div>
