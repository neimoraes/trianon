  <h2 class="pt-5 pb-4">Exame de Paternidade por DNA</h2>
  <p class="whitespace-tn">– Na coleta para a análise do DNA, não é necessário jejum e não há restrições quanto ao uso de drogas e medicamentos.
– O uso de qualquer medicamento, drogas, ingestão de bebidas alcoólicas, idade ou comportamento social não afetará o exame.
  </p>
  <h4 class="py-2">Possibilidades de realização do teste:</h4>
  <p class="whitespace-tn pl-4">•	Trio: exame de Investigação de Paternidade do qual participa mãe, filho (a) e suposto pai;
•	Duo: exame de Investigação de Paternidade do qual participa suposto pai e filho (a) ou suposta mãe e filho (a). O exame pode ser realizado caso a criança esteja registrada em nome do suposto pai,  caso contrário, será necessário o consentimento do responsável legal pela criança ou autorização judicial. Se a requerente for maior de 18 anos nenhuma solicitação é necessária.
•	Reconstrução: nos casos em que o suposto pai é falecido, existe a possibilidade de ser feita a reconstrução de DNA através de coleta de material de familiares do mesmo, entrar em contato com o nosso laboratório para ver as possibilidades.
</p>

  <h3 class="py-2">AGENDAMENTO</h3>
  <p class="whitespace-tn">– O teste de DNA pode ser agendado e realizado em qualquer uma de nossas unidades, mais informações pelo WhatsApp (18 99794-0595).
– As coletas judiciais são marcadas pelo próprio fórum.
</p>

  <h3 class="py-2">DOCUMENTAÇÃO</h3>
  <p class="whitespace-tn">– Cópia de documento de identificação com foto e assinatura de cada envolvido com a apresentação do original no momento da coleta.
– No caso de menores de idade serão aceitas certidão de nascimento ou declaração de nascido vivo (quando não for registrado). NÃO serão aceitos declarações de hospitais, carteiras vacinais, etc.
– Para coletas em que o filho (quando não registrado no nome do suposto (a) pai ou mãe), suposto (a) pai ou mãe são menores de idade é obrigatória à autorização por escrito de um dos pais ou de um responsável legal, bem como a apresentação da documentação deste responsável, com foto e assinatura no momento da coleta e envio da cópia legível ou solicitação judicial ou Conselho Tutelar.
</p>



  <h5 class="py-2">OBS: Os pacientes que forem realizar o exame, devem apresentar cópia e DOCUMENTO OFICIAL COM FOTO (RG ou CNH). Sem o DOCUMENTO OFICIAL, o paciente não poderá ser atendido. </h5>

  <p class="whitespace-tn">Na dúvida, entrar em contato com o laboratório através de nossa central de agendamento ou pelo e-mail no espaço Fale Conosco.</p>

