<h2 class="pt-5 pb-4">Estou grávida, e agora?</h2>

<p class="whitespace-tn">O primeiro exame a ser realizado por uma mulher grávida é o teste de gravidez. Apesar de haver testes em farmácia, aconselha-se visitar um médico e realizar o exame de sangue denominado βHCG quantitativo. Após pelo menos 2 semanas de atraso menstrual deve-se realizar uma ultrassonografia para a confirmar a idade gestacional e sua localização.

Em caso de gravidez planejada, o ideal é fazer uma consulta pré-concepcional em que serão solicitados exames de sangue e prescrita vitamina B9 (ácido fólico). Desse modo, a mulher se certifica de que está bem e é capaz de ter uma gestação saudável. O ácido fólico é uma vitamina importante para prevenir defeitos no sistema nervoso central, sendo caso mais grave a anencefalia.

<i>A rotina de pré-natal varia de acordo com o grau de risco da gestação. Nela inclui, além dos exames solicitados em todos os trimestres da gravidez, visitas periódicas (mensais) ao obstetra. </i>
</p>

<h3 class="py-2">PRÉ-NATAL</h3>
<p class="whitespace-tn"> <i>A rotina de pré-natal varia de acordo com o grau de risco da gestação. Nela inclui, além dos exames solicitados em todos os trimestres da gravidez, visitas periódicas (mensais) ao obstetra. </i>

Toda gestante (mulher grávida) deve fazer pré-natal para acompanhar o desenvolvimento do bebê e a sua saúde. O principal exame durante a gravidez é a ultrassonografia. Esse exame é capaz de avaliar alterações no crescimento, na anatomia e vitalidade do seu bebê, além de mostrar o sexo. Quem determina a quantidade e a frequência com que devem ser feitos os exames é o seu médico obstetra, responsável pelas suas consultas de pré-natal.

Além dos exames de ultrassonografia, uma lista de exames laboratoriais será solicitada pelo teu obstetra, que considerará os exames mais adequados para cada trimestre da gestação, levando em consideração se ela é de baixo ou alto risco. Esses exames são solicitados a partir de recomendações do Ministério da Saúde.
</p>

<h3 class="py-2">Exames para uma gravidez de baixo risco.</h3>

<h4 class="pb-2">1º trimestre</h4>

<p class="whitespace-tn">Determinação de grupo sanguíneo e do fator Rh: para evitar a incompatibilidade sanguínea (também chamada de Eritroblastose Fetal). A Eritroblastose Fetal pode ser evitada através de profilaxia em pacientes que sejam Rh negativo, pois, se o feto for Rh positivo, e esse sangue entrar em contato com sangue materno, numa segunda gestação pode ocorrer de anticorpos maternos “atacarem” o sangue do bebê.

Hemograma: é importante para o diagnóstico de anemia;

Glicemia de jejum: para rastreamento de diabetes gestacional ou até mesmo diabetes Mellitus;

Sorologias: hepatite B e C, toxoplasmose, HIV, rubéola e VDRL (sífilis);

Exame de urina: rastreamento de infecção urinária, as gestantes com infecção urinária, mesmo que sem sintomas, devem ser tratadas.
</p>

<h4 class="py-2">2º trimestre</h4>

<p class="whitespace-tn">Glicemia de jejum ou teste de tolerância a glicose (GTT 75g), esse exame deve ser feito entre 24 e 28 semanas de gestação para rastreamento de diabetes gestacional, nos casos de glicemia de jejum normal no início do pré-natal;

Sorologias: VDRL (sífilis), toxoplasmose (caso paciente seja susceptível, ou seja, no exame no primeiro trimestre IgG e IgM foram negativos para toxoplasmose)

Exame de urina.
</p>

<h4 class="py-2">3º trimestre</h4>

<p class="whitespace-tn">Hemograma;

Sorologias: hepatite B e C, toxoplasmose (paciente susceptível), HIV e VDRL (sífilis);

Exame de urina.
 </p>
