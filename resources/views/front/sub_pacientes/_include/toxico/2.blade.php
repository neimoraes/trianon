    <h4 class="py-2">O exame toxicológico requer algum tipo de preparação?</h4>

    <p class="pl-4 whitespace-tn">O exame não requer nenhum tipo de preparação prévia, por esta razão, não há, nem mesmo, a necessidade de agendar a coleta, que é a primeira de duas etapas:

    •	Primeira etapa: coleta de amostra de cabelo ou pelo;

    •	Segunda etapa: a análise toxicológica do material coletado efetuada por laboratórios parceiros.
    </p>

    <h4 class="py-2">Coleta</h4>

    <p class="pl-4 whitespace-tn">A etapa de coleta é muito simples e totalmente indolor e não exige nenhum tipo de preparação prévia. O uso de produtos como gel, shampoo, condicionador ou tintura não influenciam no resultado do teste.

O cabelo pode estar: tingido; alisado; com permanente; com gel. Entretanto, para garantir que a coleta de cabelo seja realizada com sucesso, é necessária atenção ao cabelo que não pode estar molhado, úmido e precisa estar com pelo menos, 3 cm de comprimento.

Além disso, é necessário também que a análise seja realizada no próprio cabelo do doador, não podendo ser aplique de cabelo, seja ele sintético ou natural.

A utilização de desodorante corporal ou cremes, também não interferem no resultado do exame toxicológico, no caso de a análise ser feita em pelos.

Outra dica muito útil: vestir-se com roupas confortáveis. Dessa forma, caso não seja possível realizar a coleta de cabelo, o coletor poderá realizar a coleta de pelos, nas pernas, braços, tórax ou axilas, com mais facilidade.
</p>
