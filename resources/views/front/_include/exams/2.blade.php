            <div class="whitespace-tn text-justify"> O DNA é encontrado em todas as células nucleadas do corpo na mesma forma. O DNA é o mesmo encontrado no sangue, saliva, pele, tecidos, dentes, ossos e no sêmen. O DNA é formado na concepção, isto é, na união dos gametas para a formação de um indivíduo e permanecendo constante, tanto em uma pessoa viva, quanto falecida.
              O exame de DNA (ácido desoxirribonucleico) é o teste mais seguro disponível para estudo da Paternidade. Possui um alto poder de discriminação, cada pessoa possui um DNA único, com exceção de gêmeos idênticos.
              Para realização do teste, todos os indivíduos envolvidos (mãe, filho e suposto pai) devem realizar a coleta para o teste, que pode ser feita por punção venosa, punção digital ou por swab (mucosa bucal).  Caso a mãe não queira participar ou seja falecida, poderá ser realizada a coleta de amostra do suposto pai e a criança. O exame pode ser realizado caso a criança esteja registrada em nome do suposto pai,  caso contrário, será necessário o consentimento do responsável legal pela criança ou autorização judicial. Se a requerente for maior de 18 anos nenhuma solicitação é necessária. Em casos em que o suposto pai é falecido, existe a possibilidade de ser feita a reconstrução de DNA através de coleta de material de familiares do mesmo.
