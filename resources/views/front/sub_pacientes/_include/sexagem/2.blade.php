  <h2 class="pt-5 pb-4">O que é o Exame de Sexagem Fetal?</h2>



  <p class="whitespace-tn">É o exame capaz de determinar o sexo do feto de forma precoce, através do sangue materno.
  </p>




  <h3 class="py-2">Como funciona?</h3>



  <p class="whitespace-tn">Durante a gestação, ocorre um fluxo fisiológico de pequena quantidade de células fetais para a circulação da mãe. O teste baseia-se na identificação de marcadores do cromossomo Y na amostra de sangue materno coletada, sem oferecer risco para o feto ou para a mãe. Considerando que apenas indivíduos do sexo masculino possuem esse cromossomo, sua identificação no teste indica que o feto é do sexo masculino. Já a ausência de detecção é indicativa de gestação do sexo feminino. Devido ao princípio do método, o teste não deve ser feito quando a gestante se encontra em condições especiais, como período pós-transfusão de sangue, transplante de medula óssea, infusão ou imunização com linfócitos paternos.</p>


  <h3 class="py-2">Benefícios</h3>



  <p class="whitespace-tn">• <strong> Detecção precoce do sexo do bebê (a partir da 8ª semana completa de gestação)</strong>;
 • Método minimamente invasivo;
 • Sem necessidade de jejum ou qualquer outra preparação especial;
• Válido para todas as gestações, inclusive gemelares (univitelina);
• <strong>Excelente grau de acerto (99% após 8ª semana de gestação)</strong>;
• Processamento da amostra feito exclusivamente por indivíduos do sexo feminino, minimizando as chances de contaminação (resultados falso-positivos para sexo masculino)
</p>
