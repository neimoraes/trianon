  <h2 class="py-0 my-0 h4">O exame toxicológico é obrigatório para motoristas habilitados nas categorias C, D e E da CNH. Saiba agora, tudo sobre o exame toxicológico CNH e saiba como conseguir sua CNH mais rápido, sem atrasos e sem riscos.</h2>

  <h3 class="pt-5 pb-4">O que é o exame toxicológico?</h3>

  <p class=" whitespace-tn">O exame toxicológico é uma exigência a todo o motorista que pretende obter a CNH – Carteira Nacional de Habilitação, – nas categorias: C, D ou E.

Tornou-se obrigatório após a publicação da Lei Federal 13.103/2015, mais conhecida como Lei do Caminhoneiro ou Lei do Motorista.

Portanto, o motorista deve submeter-se ao exame toxicológico nos casos de: obtenção da:

•	Carteira Nacional de Habilitação – CNH;
•	Alteração de categoria da CNH;
•	Renovação da CNH.

A Lei 13.103/2015 (Lei do Caminhoneiro) é válida em todo o Brasil desde março de 2016.  Logo após, em 2017 o exame toxicológico passou a ser exigido também nas admissões e desligamentos de motoristas profissionais contratados no regime CLT.
Aliás, o exame toxicológico exigido para fins de emissão de CNH detecta o uso de drogas em um período que pode variar entre 90 e 180 dias antes da coleta. Por esta razão, o exame também é conhecido como exame toxicológico de larga janela de detecção.
determinação a Lei 13.103 de 2015, somente o toxicológico de larga janela, atende os requisitos necessários para obtenção e renovação da CNH.
</p>
  <h3 class="py-4">Como o exame toxicológico é feito?</h3>
  <p class=" whitespace-tn">Primeiro, é necessário saber que o exame toxicológico só pode ser realizado em laboratórios devidamente credenciados pelo Departamento Nacional de Trânsito (DENATRAN).

Ele é realizado a partir da coleta de uma pequena amostra de cabelos ou pelos. Em síntese, o cabelo é coletado com o auxílio de uma tesoura. Portanto, corta-se uma fina mecha de cabelo próximo à raiz ou então, raspa-se pelos de uma parte do corpo (peito, pernas, braços, axilas).

É um procedimento livre da utilização de agulhas, o exame toxicológico é indolor e não invasivo, tornando-se impossível perceber que o doador realizou o exame.
</p>



