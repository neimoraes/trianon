            <a class="d-flex align-items-center" data-toggle="collapse" href="#collapsemedicamentos" role="button" aria-expanded="false" aria-controls="collapsemedicamentos">
            <h3 class="h5 py-3 m-0">O paciente deve suspender os medicamentos antes da coleta de sangue?</h3>
             </a>
             <div class="underline-tn">
             </div>
            <div class="collapse" id="collapsemedicamentos">
              Não. A suspensão de medicamentos somente pode ser autorizada pelo médico assistente e seu uso deve ser mantido conforme a orientação do mesmo. Alguns exames são realizados exatamente para avaliar o efeito do uso do medicamento. Para drogas de monitorização terapêutica, cuja coleta de sangue é sugerida imediatamente antes da próxima dose, é conveniente o paciente trazer consigo o medicamento em uso, para evitar ultrapassar o horário programado para a medicação.
              <div class="underline-tn">
              </div>
          </div>
          </div>


    <div class="col">
        <a class="d-flex align-items-center" data-toggle="collapse" href="#collapsedna" role="button" aria-expanded="false" aria-controls="collapsedna">
          <h3 class="h5 py-3 m-0">Até aspirina altera resultados de exames?</h3>
         </a>
         <div class="underline-tn">
         </div>
         <div class="collapse" id="collapsedna">
          <div class="whitespace-tn text-left"> Com certeza. Aspirina é o nome popular do ácido acetilsalicílico. Ela está presente em muitos analgésicos e antitérmicos, tais como AAS, Buferin, Doril, Melhoral, Aspirina Forte, Cibalena, Doloxene-A e Aspirina C. Mas também em antiácidos (Alka-Seltzer e Engov), onde está associada a outras substâncias farmacológicas. Por isso, guarde bem: todos os remédios com ácido acetilsalicílico interferem nos exames de coagulação do sangue. Em altas doses, podem diminuir os valores totais de tiroxina ou T4, um dos hormônios da tireoide.</div>
          <div class="underline-tn">
          </div>
        </div>
