    <h2 class="pb-4">Escolha o tipo de exame:</h2>
  <div class="examespreparese-tn">


    <a class="" data-toggle="collapse" href="#collapsesangue" role="button" aria-expanded="false" aria-controls="collapsesangue">
      <h3 class="h5 py-2">Exames de sangue em geral</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapsesangue">
      <p class="whitespace-tn">A coleta de sangue deve ser realizada preferencialmente com o paciente em jejum, de acordo com o exame a ser realizado:
      - 4h para coleta de sangue que tenha cortisol, lembrando que a mesma deverá ser feita até às 9h da manhã (idealmente as 8h da manhã), além de ser imprescindível informar os medicamentos em uso (inclusive pomadas e cremes) dia e hora da última dose;
      - 4h para dosagem de prolactina, recomendando-se um repouso de 30 minutos antes da coleta para quem fez exercícios físicos ou conforme orientação do médico que solicitou o exame;
      - 8h para dosagem de glicose, ferro e colesterol Total e frações;
      - 12h para dosagem de Triglicérides;
      - 06h para as demais coletas.
      Atualmente já existem valores de referência para o Colesterol Total, Frações do Colesterol e Triglicérides quando a coleta é feita sem o jejum e conforme orientação do médico que solicitou o(s) exames.
      </p>
    </div>

    <a class="" data-toggle="collapse" href="#collapseglicemia" role="button" aria-expanded="false" aria-controls="collapseglicemia">
      <h3 class="h5 py-2">Coleta para a glicemia pós-prandial</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseglicemia">
      <p class="whitespace-tn">Este exame exige duas coletas de sangue: a primeira deve ser feita com o paciente em jejum de 08 horas. Após esta coleta, o paciente deverá fazer a refeição normalmente do seu dia a dia (almoço), em seu horário habitual, preferencialmente até o meio-dia. Duas horas após esta refeição, deve comparecer ao laboratório e será feita a segunda coleta de sangue para a dosagem da glicemia.</p>
    </div>

    <a class="" data-toggle="collapse" href="#collapseurinacoletor" role="button" aria-expanded="false" aria-controls="collapseurinacoletor">
      <h3 class="h5 py-2">Coleta de urina por meio de saco coletor (para criança)</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseurinacoletor">
      <p class="whitespace-tn">Fazer a higiene da região genital com água e sabão e não usar anti-sépticos. Nos meninos, retrair o prepúcio e lavar bem. Nas meninas, limpar sempre de cima para baixo, trocando a compressa sempre que voltar ao local onde iniciou a limpeza. Enxugar com toalha limpa ou gaze. COLETA: Retirar o papel que recobre a parte adesiva e fixar o orifício do saco coletor. Assim que a criança urinar no saco, deve-se fechá-lo colando as bordas do orifício e verificar se está bem vedado. A urina deve ser mantida em geladeira até ser levada ao laboratório.</p>
    </div>



    <a class="" data-toggle="collapse" href="#collapsepsa" role="button" aria-expanded="false" aria-controls="collapsepsa">
      <h3 class="h5 py-2">PSA Total e Livre</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapsepsa">
      <p class="whitespace-tn">A coleta de sangue para a dosagem do PSA não exige jejum absoluto, só não devendo ser feita após uma refeição, assim, evita-se que uma possível lipemia atrapalhe a dosagem. A coleta deve ser feita observando as seguintes instruções: - Após ejaculação, aguardar 48 horas;
      - Após exercícios pesados, aguardar 24 horas;
      - Após ter andado de bicicleta (inclusive ergométrica), motocicleta ou cavalo, aguardar 48 horas;
      - Após toque retal, aguardar 72 horas;
      - Após biópsia de próstata, aguardar 04 semanas;
      - Após massagem na próstata, aguardar 04 semanas;
      - Após realizar ultra som transretal, aguardar 07 dias.
      </p>
    </div>

    <a class="" data-toggle="collapse" href="#collapseurina" role="button" aria-expanded="false" aria-controls="collapseurina">
      <h3 class="h5 py-2">Coleta para exame de urina (EAS)</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseurina">
    <p class="whitespace-tn">A amostra deverá ser coletada pelo paciente na manhã do dia destinado a entrega do mesmo (primeira urina do dia). Fazer a higiene da região genital apenas com água e sabão. Desprezar o primeiro jato urinário e colher o restante, o suficiente para encher o recipiente adequado. Fazer a entrega do material no laboratório até 1h após a coleta. Caso não seja possível, manter a amostra refrigerada, entregando assim que possível.</p>
    </div>

    <a class="" data-toggle="collapse" href="#collapseurocultura" role="button" aria-expanded="false" aria-controls="collapseurocultura">
      <h3 class="h5 py-2">Cultura de Urina ou Urocultura</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseurocultura">
        <p class="whitespace-tn">A amostra de urina para exame deverá ser coletada necessariamente na manhã do dia destinado a entrega do material (primeira urina do dia). Fazer a higiene da região genital com bastante água e sabão (não utilizar qualquer antisséptico) e colher no recipiente estéril entregue pelo laboratório, coletar somente o jato médio da urina, desprezando a primeira porção da micção. </p>
    </div>




    <a class="" data-toggle="collapse" href="#collapseurina12" role="button" aria-expanded="false" aria-controls="collapseurina12">
      <h3 class="h5 py-2">Urina de 12 horas</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseurina12">
      <p class="whitespace-tn">Desprezar a primeira urina do dia e marcar o horário. Daí em diante, recolher todas as micções durante 12 horas. Toda urina recolhida deve ser conservada em geladeira e entregue ao laboratório após a última coleta. Identificar o recipiente com o nome completo, peso e altura do paciente.</p>
    </div>

    <a class="" data-toggle="collapse" href="#collapseurina24" role="button" aria-expanded="false" aria-controls="collapseurina24">
      <h3 class="h5 py-2">Coleta de urina de 24 horas</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseurina24">
      <p class="whitespace-tn">Pela manhã, esvaziar a bexiga desprezando toda a urina e marcando a hora exata em que terminou de urinar. A partir da hora marcada, coletar todas as urinas eliminadas pelas próximas 24 horas em um recipiente plástico, mantendo as amostras coletadas refrigeradas em geladeira. Por exemplo, se a coleta teve início às 08:00 horas, deve-se coletar a urina até às 08:00 horas da manhã seguinte, inclusive. Se houver solicitação de Clearance de Creatinina ou de outros tipos, o paciente deve comparecer ao laboratório com a amostra da urina coletada e em jejum de 6 horas para proceder a coleta de sangue. Completada a coleta, trazer toda a urina imediatamente ao laboratório.</p>
    </div>

    <a class="" data-toggle="collapse" href="#collapsefezesepf" role="button" aria-expanded="false" aria-controls="collapsefezesepf">
      <h3 class="h5 py-2">Coleta de fezes (Exame parasitológico de fezes - EPF)</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapsefezesepf">
      <p class="whitespace-tn">Realizar a coleta preferencialmente pela manhã no dia da entrega ao laboratório. Apanhar 03 (três) porções pequenas do mesmo material fecal, em posições diferentes (começo, meio e fim das fezes) e coloca-las em recipiente adequado (oferecido pelo laboratório).
      Se for preciso coletá-las no dia anterior, mantê-las armazenadas na geladeira.</p>
    </div>

    <a class="" data-toggle="collapse" href="#collapsesangueoculto" role="button" aria-expanded="false" aria-controls="collapsesangueoculto">
      <h3 class="h5 py-2">Coleta de fezes para pesquisa de sangue oculto</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapsesangueoculto">
      <p class="whitespace-tn">As fezes entregues ao laboratório devem ser recentemente coletadas – “fezes frescas”. Colocar as fezes em recipiente adequado (oferecido pelo laboratório), sem enchê-lo, pois a quantidade de amostra necessária para a análise é pequena. Não coletar durante o período menstrual ou quando houver sangramento local. Evitar o uso de medicamentos irritantes do trato gastrointestinal (analgésicos, anti-inflamatórios e vitamina C).
       OBS: Existe atualmente técnica utilizada (Imunocromatografia) que não exige dieta alimentar para realização do exame.
       Consulte o laboratório.
     </p>
    </div>

    <a class="" data-toggle="collapse" href="#collapsegordurafecal" role="button" aria-expanded="false" aria-controls="collapsegordurafecal">
      <h3 class="h5 py-2">Coleta de fezes para dosagem de gordura fecal</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapsegordurafecal">
      <p class="whitespace-tn">  Antes de coletar as fezes, se necessário, urinar no vaso sanitário
      para evitar a contaminação do material. Em casos de crianças utilizar
      coletor de urina, se necessário.
      - Evitar o uso de talco, laxantes, antiácidos, contraste oral ( utilizado em exames radiológicos) e supositórios nos 3 dias que antecedem ao exame e no dia da coleta.
      - Defecar em vasilhame limpo e seco.
      - Entregar no laboratório em até 2 horas após a coleta em temperatura ambiente, após este período refrigerar.
      - Poderá ser mantido ate 3 dias refrigerado entre 2 e 8 ºC.
      </p>
    </div>

    <a class="" data-toggle="collapse" href="#collapseescarro" role="button" aria-expanded="false" aria-controls="collapseescarro">
      <h3 class="h5 py-2">Coleta de Escarro (Pesquisa de BAAR, Cultura para BK, Pesquisa direta de fungos, Bacterioscopia - GRAM)</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapseescarro">
      <p class="whitespace-tn">A coleta deve ser feita preferencialmente pela manhã ao acordar, antes da ingestão de alimentos sólidos ou líquidos. Fazer higiene oral com bochechos e gargarejos somente com água. Inspirar profundamente algumas vezes e tossir procurando expectorar a quantidade máxima de secreção dos pulmões. Recolher o catarro diretamente no recipiente estéril adequado, tampando-o imediatamente. é importante lembrar da necessidade de coletar escarro e não saliva, para não invalidar o resultado do exame.
      </p>
    </div>

    <a class="" data-toggle="collapse" href="#collapsefungos" role="button" aria-expanded="false" aria-controls="collapsefungos">
      <h3 class="h5 py-2">Pesquisa e coleta para fungos</h3>
    </a>
    <div class="underline-tn">
    </div>
    <div class="collapse" id="collapsefungos">
      <p class="whitespace-tn"> Suspender o uso de medicamentos antimicóticos, de esmalte (caso a coleta seja nas unhas), cremes e loções no local com, no mínimo, 15 (quinze) dias de antecedência.
      </p>
    </div>

